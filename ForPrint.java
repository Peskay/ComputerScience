public class ForPrint {
    public static void main(String[] args) {
        for(int line = 1; line < 13; line++) {
                switch (line) {
                case 1:  line = 1;
                        System.out.println("     *     ");
                        break;
                case 2:  line = 2;
                        System.out.println("    ***    ");
                        break;
                case 3:  line = 3;
                        System.out.println("   *****   ");
                        break;
                case 4:  line = 4;
                        System.out.println("  *******  ");
                        break;
                case 5:  line = 5;
                        System.out.println(" ********* ");
                        break;
                case 6:  line = 6;
                        System.out.println("***********");
                        break;
                case 7:  line = 7;
                        System.out.println("***********");
                        break;
                case 8:  line = 8;
                        System.out.println(" ********* ");
                        break;
                case 9:  line = 9;
                        System.out.println("  *******  ");
                        break;
                case 10: line = 10;
                        System.out.println("   *****   ");
                        break;
                case 11: line = 11;
                        System.out.println("    ***    ");
                        break;
                case 12: line = 12;
                        System.out.println("     *     ");
                        break;
            }
        }
    }
}

